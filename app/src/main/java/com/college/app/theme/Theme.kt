@Composable
fun CollegeAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    MaterialTheme(
        colors = if (darkTheme) collegeAppLightColors() else collegeAppLightColors(),
        typography = CollegeAppTypography,
        content = content
    )
}